# Using while loop to print numbers from 1 to 10
num = 1
while num <= 10:
    print(num, end=" ")
    num += 1

print()  # Move to next line

# List of names
names = ["Alice", "Bob", "Charlie", "David", "Eve"]

# Using for loop to print personalized greetings
for name in names:
    print(f"Hello, {name}!", end=" ")
